package br.com.javalang;

public class CalculosMath {

	public static void main(String[] args) {
		
		System.out.println(Math.E);
		System.out.println(Math.PI);
		System.out.println(Math.sin(35));
		System.out.println(Math.acos(10));
		System.out.println(Math.cos(2));
		System.out.println(Math.tan(20));
		System.out.println(Math.round(Math.PI));
		System.out.println(Math.ceil(Math.PI));
		System.out.println(Math.floor(Math.PI));
		System.out.println(Math.pow(2, 3));
		System.out.println(Math.round(Math.pow(2, 3)));
		System.out.println(Math.floor(Math.pow(2, 3)));
		System.out.println(Math.sqrt(10));
		System.out.println(Math.round(Math.sqrt(10)));
	}

}
